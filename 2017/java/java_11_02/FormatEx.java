package java_11_02;

public class FormatEx {

	public static void main(String[] args) {
		int num1 = 13;
		int num2 = 18;
		int num3 = 22;
		int num4 = 33;
		
		System.out.println("num1 = " + num1 + " num2 = " + num2
				+ " num3 = " + num3 + " num4 = " + num4);
		System.out.printf("num1 = %d num2 = %d num3 = %d num4 = %d \n test", 
				num1, num2, num3, num4);
	}

}
